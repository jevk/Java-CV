package structs;

public class Degree {
    // Variable declarations
    // Store the school name
    public String school;
    // Store the education (such as "Software Engineering")
    public String education;
    // Store the degree (such as "IT Specialist")
    public String degree;
    // Store the starting year of the studies
    public String startYear;
    // Store the graduation year of the studies
    public String endYear;


    // Constructors
    public Degree() {
        // Initialize variables
        school = "";
        education = "";
        degree = "";
        startYear = "";
        endYear = "";
    }
}